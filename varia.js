//Primitive Data Types

//1.String
let str = "My name is vansh singh"
console.log(str)
console.log(typeof(str))

//2.Number
let no = 67
console.log(no)
console.log(typeof(no))

//3.Boolean
let bol = true
console.log(bol)
console.log(typeof(bol))

//4.Undefined
let und = undefined
console.log(und)
console.log(typeof(und))

//5.Null
let nu = null
console.log(nu)
console.log(typeof(nu))

//Reference Data Types

//1.Array
let arr = ["March",1,45.7,true,]
console.log(arr)
console.log(typeof(arr))

//2.Object
let obj = {"March":"Java","april":"JavaScript"}
console.log(obj)
console.log(typeof(obj))

//3.Date
let dat = new Date()
console.log(dat)
console.log(typeof(dat))

